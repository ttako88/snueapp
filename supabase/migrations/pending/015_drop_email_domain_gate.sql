-- ============================================================
-- 015_drop_email_domain_gate.sql — 이메일 도메인 제한 폐기
-- ============================================================
-- ⚠️ pending/ 에 둔다. 아직 적용하지 않는다. 적용 조건은 아래 "게이트" 참조.
--
-- 배경
--   v2 RFC 확정사항: 가입을 snue.ac.kr 로 제한하지 않는다. 소속 증명은
--   이메일 도메인이 아니라 학생 인증(학번+증빙서류)이 담당한다. 도메인
--   제한은 (a) 학교 메일을 안 쓰는 재학생을 배제하고 (b) 졸업생·교직원
--   경계를 못 다루며 (c) 메일 발급만 되면 통과라 증명력도 약하다.
--
--   010 은 그 RFC 와 모순되는 방향으로 보이지만 의도된 순서다. reset 이
--   구 스키마의 트리거를 지웠고 001~009 가 복원하지 않아, 그 시점에는
--   문지기가 아예 없었다. 인증 파이프라인이 없는 상태에서 문을 활짝 열어
--   두는 것보다 임시 자물쇠를 다시 채우는 편이 나았다.
--
-- 게이트 — 아래가 전부 참일 때만 적용한다
--   1. SUPABASE_SECRET_KEY 가 운영 Vercel 에 등록돼 /api/verification/*
--      라우트가 503 이 아닐 것
--   2. VERIFY_HMAC_KEY_V1 / VERIFY_HMAC_CURRENT_VER 운영 등록
--   3. diag-verification-ready.mjs 가 VERIFICATION_READY=PASS
--   4. 실제 제출 → 심사 → 승인 E2E 1회 성공
--
--   이 순서를 지키지 않으면 "아무나 가입 가능 + 인증 불가" 구간이 생긴다.
--   그 구간에서 들어온 계정은 나중에 걸러낼 방법이 없다.
--
-- 함께 바꿔야 하는 코드 (같은 배포에 포함)
--   app/login/page.js — shouldCreateUser: isSnue 를 true 로. 지금은
--     비-snue 주소로 신규 가입이 막히므로, DB 만 풀면 화면이 여전히 막는다.
--     한쪽만 바꾸면 "왜 안 되는지 알 수 없는" 상태가 된다.
--
-- 되돌리기
--   010 을 다시 실행하면 트리거가 복원된다. 단 그 사이 가입한 비-snue
--   계정은 소급 차단되지 않는다(010 이 기존 행을 검사하지 않으므로).
-- ============================================================

begin;

-- 트리거만 제거하고 함수는 남긴다. 되돌릴 때 010 을 다시 돌리면 되지만,
-- 함수가 남아 있으면 create trigger 한 줄로도 복구할 수 있다.
drop trigger if exists enforce_snue_email on auth.users;

-- 함수는 유지하되, 더 이상 어디서도 쓰이지 않음을 코멘트로 남긴다.
-- 나중에 "이 함수 왜 있지" 하고 지우려는 사람에게 맥락을 준다.
comment on function private.enforce_snue_email() is
  '2026-07 폐기(015). 도메인 제한 대신 학생 인증으로 소속을 검증한다. '
  'create trigger 로 재부착하면 즉시 복구 가능 — 010 참조.';

commit;
