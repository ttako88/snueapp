// 여기만 고치면 목록·홈·상세 어디서나 같은 색이 적용됨.
//
// 색은 전부 톤다운(연한 파스텔 배경 + 중간톤 글자)으로,
// 우리 앱의 파란 톤과 어울리게 골랐음. 6종 → 4색 계열로 묶음:
//   중요공지=분홍(강조) / 수강신청=파랑 / 교육실습·교육봉사=초록
//   행사안내=노랑 / 기타안내=회색(말그대로 '기타')

export const CATEGORY_STYLES = {
  중요공지: { bg: "#ffe4ef", text: "#d64c86" }, // 🩷 분홍 (강조)
  수강신청: { bg: "#e6f2fc", text: "#1f82c8" }, // 💙 파랑
  교육실습: { bg: "#e4f3e9", text: "#3b8b5d" }, // 💚 초록
  교육봉사: { bg: "#e4f3e9", text: "#3b8b5d" }, // 💚 초록
  행사안내: { bg: "#fbf1d3", text: "#96760f" }, // 💛 노랑
  기타안내: { bg: "#eef1f4", text: "#64748b" }, // ⬜ 회색
};

// 위 표에 없는 낯선 구분이 와도 안전하게 회색으로
const DEFAULT_STYLE = { bg: "#eef1f4", text: "#64748b" };

export function categoryStyle(category) {
  return CATEGORY_STYLES[category] || DEFAULT_STYLE;
}

